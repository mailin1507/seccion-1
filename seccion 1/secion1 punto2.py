# Cree un programa que lea dos números y muestre su producto, su cociente, su módulo, su suma y su resta
numero1=int(input("ingrese el numero 1:"))
numero2=int(input("ingrese el numero 2:"))
suma=numero1+numero2
resta=numero1-numero2
multiplicacion=numero1*numero2
division=numero1/numero2
modulo=numero1%numero2
print("El resultado de la suma es" +str(suma)+(" El resultado de la resta es") +str(resta)+(" El resultado de la multiplicacion es ") +str(multiplicacion)+(" El resultado de la division") +str(division)+(" El resultado del modulo") +str(modulo))

