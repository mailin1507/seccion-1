""" Cree un programa que lea la edad de un usuario y muestre cuántos años tendrá el usuario dentro
 de tantos años como el usuario indique. """

edad=int(input("ingrese su edad"))
edadFuturo=int(input("ingrese cuantos años quiere transcurrir"))

total=edad+edadFuturo

print("su edad al pasar "+str(edadFuturo)+(" años sera ")+str(total))



 